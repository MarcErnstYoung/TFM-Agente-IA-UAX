@app.route("/api/predicted", methods=["GET"])
def api_predicted():
    """
    Returns classified events from alertas_predicted.

    Query params:
      ?verdict=all|tp|fp      (default: all)
      ?limit=N                (default: 100)
      ?offset=N               (default: 0,  for pagination)
      ?date_from=YYYY-MM-DD   (optional)
      ?date_to=YYYY-MM-DD     (optional)
      ?client=CLIENT_NAME     (optional, filter by orig_client)

    Response shape:
    {
        "total":  int,   -- total rows matching filter (before limit/offset)
        "count":  int,   -- rows returned in this page
        "rows": [
            {
                "source_event_id": str,
                "orig_client":     str | null,
                "orig_rule_title": str | null,
                "severity":        str | null,
                "src_ip":          str | null,
                "dest_ip":         str | null,
                "action":          str | null,
                "signature":       str | null,
                "country":         str | null,
                "prediction":      bool,
                "score":           float | null,
                "processing_seconds": float | null,  -- pipeline time for this alert
                "created_at":      str
            },
            ...
        ]
    }
    """
    try:
        verdict    = request.args.get("verdict", "all")
        limit      = min(int(request.args.get("limit",  100)), 500)
        offset     = int(request.args.get("offset", 0))
        date_from  = request.args.get("date_from")
        date_to    = request.args.get("date_to")
        client     = request.args.get("client")

        conditions = []
        params     = []

        if verdict == "tp":
            conditions.append("prediction = true")
        elif verdict == "fp":
            conditions.append("prediction = false")

        if date_from:
            conditions.append("created_at::date >= %s")
            params.append(date_from)

        if date_to:
            conditions.append("created_at::date <= %s")
            params.append(date_to)

        if client:
            conditions.append("orig_client = %s")
            params.append(client)

        where = ("WHERE " + " AND ".join(conditions)) if conditions else ""

        # Total count (for pagination)
        count_row = query_one(
            get_conn,
            f"SELECT COUNT(*) AS total FROM predicted.eventos {where}",
            params
        )
        total = int(count_row["total"] or 0)

        # Actual rows
        rows = query(
            get_conn,
            f"""
            SELECT source_event_id, orig_client, orig_rule_title, rule_description,
                   severity, src_ip, dest_ip, dest_port, action, signature, country,
                   "user", hostname, sender, subject, recipient, url,
                   prediction, score, created_at, processing_seconds
            FROM predicted.eventos
            {where}
            ORDER BY created_at DESC
            LIMIT %s OFFSET %s
            """,
            params + [limit, offset]
        )

        return jsonify({"total": total, "count": len(rows), "rows": serialize_rows(rows)})

    except Exception as e:
        return jsonify({"error": str(e)}), 500


