@app.route("/event", methods=["POST"])
def receive_event():
    data = request.get_json(silent=True)

    if not data:
        return jsonify({"error": "Body JSON requerido"}), 400

    if "event_id" not in data:
        return jsonify({"error": "event_id requerido"}), 400

    event_id = str(data["event_id"]).strip()
    event_time = data.get("event_time")

    if not event_id:
        return jsonify({"error": "event_id vacío"}), 400

    try:
        save_event_id(event_id, event_time)
        return jsonify({"status": "ok", "event_id": event_id, "event_time": event_time}), 200
    except Exception as e:
        return jsonify({"status": "error", "error": str(e)}), 500


def save_event_id(event_id: str, event_time: str | None):
    conn = get_conn()
    try:
        with conn.cursor() as cur:
            cur.execute(
                """
                INSERT INTO ids.eventos (source_event_id, event_time)
                VALUES (%s, %s)
                ON CONFLICT (source_event_id) DO UPDATE
                SET event_time = EXCLUDED.event_time;
                """,
                (event_id, event_time)
            )
        conn.commit()
    finally:
        conn.close()


