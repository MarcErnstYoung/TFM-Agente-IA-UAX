def get_conn():
    return psycopg2.connect(**DB_CONFIG)

def query(conn_fn, sql, params=None):
    """Run a SELECT and return list of dicts."""
    conn = conn_fn()
    try:
        with conn.cursor(cursor_factory=psycopg2.extras.RealDictCursor) as cur:
            cur.execute(sql, params or ())
            return [dict(r) for r in cur.fetchall()]
    finally:
        conn.close()

def query_one(conn_fn, sql, params=None):
    """Run a SELECT and return a single dict (or None)."""
    rows = query(conn_fn, sql, params)
    return rows[0] if rows else None

def serialize(obj):
    """Make datetime/date JSON-serialisable."""
    if isinstance(obj, (datetime, date)):
        return obj.isoformat()
    return obj

def serialize_rows(rows):
    return [{k: serialize(v) for k, v in row.items()} for row in rows]


