@app.route("/api/logs", methods=["GET"])
def api_logs():
    """
    Returns the last N lines of the log files.

    Query params:
      ?source=splunk|predict|all   (default: all)
      ?lines=N                     (default: 100)

    Response shape:
    {
        "logs": [
            {
                "source":    "predict",
                "filename":  "auto_predict.log",
                "lines": ["2025-05-01 12:00:00 | OK event_id=...", ...]
            },
            ...
        ]
    }
    """
    try:
        source = request.args.get("source", "all")
        n_lines = min(int(request.args.get("lines", 100)), 500)

        def read_tail(filepath, n):
            try:
                with open(filepath, "r", encoding="utf-8", errors="replace") as f:
                    return f.readlines()[-n:]
            except FileNotFoundError:
                return []

        def get_latest_log(prefix):
            """Return the active log file (logs/<prefix>.log) or most recent dated one."""
            active = os.path.join(LOG_DIR, f"{prefix}.log")
            if os.path.exists(active):
                return active
            # Fallback: most recently modified dated file
            pattern = os.path.join(LOG_DIR, f"{prefix}_*.log")
            files = glob.glob(pattern)
            return max(files, key=os.path.getmtime) if files else None

        result = []

        sources_map = {
            "splunk":  "auto_splunk",
            "predict": "auto_predict",
        }

        targets = sources_map if source == "all" else {source: sources_map[source]} if source in sources_map else {}

        for src_key, prefix in targets.items():
            filepath = get_latest_log(prefix)
            lines = read_tail(filepath, n_lines) if filepath else []
            result.append({
                "source":   src_key,
                "filename": os.path.basename(filepath) if filepath else f"{prefix}.log (not found)",
                "lines":    [l.rstrip("\n") for l in lines],
            })

        return jsonify({"logs": result})

    except Exception as e:
        return jsonify({"error": str(e)}), 500


