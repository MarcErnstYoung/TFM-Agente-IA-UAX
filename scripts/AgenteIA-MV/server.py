@app.route("/api/stats", methods=["GET"])
def api_stats():
    """
    Returns global counters used by the dashboard home page.

    Response shape:
    {
        "total_received":   int,   -- all event_ids ever received from SOAR
        "queue_pending":    int,   -- waiting in alertas_ids (not yet in Splunk)
        "queue_failed":     int,   -- failed after MAX_ATTEMPTS in alertas_ids
        "encoded_pending":  int,   -- waiting in alertas_encoded (not yet classified)
        "encoded_failed":   int,   -- failed in alertas_encoded
        "total_predicted":  int,   -- total events classified
        "total_tp":         int,   -- True Positives
        "total_fp":         int,   -- False Positives
        "tp_pct":           float, -- % of TP over total predicted
        "fp_pct":           float, -- % of FP over total predicted
        "predicted_today":  int,   -- events classified today
        "tp_today":         int,   -- TP classified today
        "fp_today":         int,   -- FP classified today
        "proc_count":       int,   -- alerts with a measured processing time
        "proc_avg":         float, -- average processing time (seconds)
        "proc_p50":         float, -- median processing time (seconds)
        "proc_p95":         float, -- 95th percentile processing time (seconds)
        "proc_min":         float, -- fastest processed alert (seconds)
        "proc_max":         float, -- slowest processed alert (seconds)
        "proc_count_today": int,   -- alerts measured today
        "proc_avg_today":   float  -- average processing time today (seconds)
    }
    Processing time = classification timestamp - moment auto_splunk picked the
    alert from ids.eventos. Queue wait time is intentionally NOT included.
    """
    try:
        # --- ids stats ---
        ids_data = query_one(
            get_conn,
            "SELECT COUNT(*) AS total FROM ids.eventos"
        )
        queue_pending = query_one(
            get_conn,
            "SELECT COUNT(*) AS cnt FROM ids.eventos WHERE attempt_count = 0 OR attempt_count IS NULL"
        )
        queue_failed = query_one(
            get_conn,
            "SELECT COUNT(*) AS cnt FROM ids.eventos_fallidos"
        )

        # --- encoded stats ---
        encoded_pending = query_one(
            get_conn,
            "SELECT COUNT(*) AS cnt FROM encoded.eventos"
        )
        encoded_failed = query_one(
            get_conn,
            "SELECT COUNT(*) AS cnt FROM encoded.eventos_fallidos"
        )

        # --- predicted stats ---
        pred_total = query_one(
            get_conn,
            "SELECT COUNT(*) AS total, "
            "  SUM(CASE WHEN prediction = true  THEN 1 ELSE 0 END) AS tp, "
            "  SUM(CASE WHEN prediction = false THEN 1 ELSE 0 END) AS fp "
            "FROM predicted.eventos"
        )
        pred_today = query_one(
            get_conn,
            "SELECT COUNT(*) AS total, "
            "  SUM(CASE WHEN prediction = true  THEN 1 ELSE 0 END) AS tp, "
            "  SUM(CASE WHEN prediction = false THEN 1 ELSE 0 END) AS fp "
            "FROM predicted.eventos "
            "WHERE created_at::date = CURRENT_DATE"
        )

        # --- processing-time stats (temps de processament per alerta) ---
        # processing_seconds = classificació - inici de processament a auto_splunk.
        # Només es comptabilitzen les alertes amb el cronòmetre mesurat (NOT NULL).
        proc = query_one(
            get_conn,
            """
            SELECT
                COUNT(processing_seconds)                                          AS cnt,
                AVG(processing_seconds)                                            AS avg_s,
                PERCENTILE_CONT(0.5)  WITHIN GROUP (ORDER BY processing_seconds)   AS p50,
                PERCENTILE_CONT(0.95) WITHIN GROUP (ORDER BY processing_seconds)   AS p95,
                MIN(processing_seconds)                                            AS min_s,
                MAX(processing_seconds)                                            AS max_s
            FROM predicted.eventos
            WHERE processing_seconds IS NOT NULL
            """
        )
        proc_today = query_one(
            get_conn,
            """
            SELECT
                COUNT(processing_seconds) AS cnt,
                AVG(processing_seconds)   AS avg_s
            FROM predicted.eventos
            WHERE processing_seconds IS NOT NULL
              AND created_at::date = CURRENT_DATE
            """
        )

        total     = int(pred_total["total"] or 0)
        tp        = int(pred_total["tp"]    or 0)
        fp        = int(pred_total["fp"]    or 0)

        def r2(v):
            """Round to 2 decimals, keeping None as None."""
            return round(float(v), 2) if v is not None else None

        return jsonify({
            "total_received":  int(ids_data["total"]       or 0),
            "queue_pending":   int(queue_pending["cnt"]    or 0),
            "queue_failed":    int(queue_failed["cnt"]     or 0),
            "encoded_pending": int(encoded_pending["cnt"]  or 0),
            "encoded_failed":  int(encoded_failed["cnt"]   or 0),
            "total_predicted": total,
            "total_tp":        tp,
            "total_fp":        fp,
            "tp_pct":          round(tp / total * 100, 1) if total else 0,
            "fp_pct":          round(fp / total * 100, 1) if total else 0,
            "predicted_today": int(pred_today["total"] or 0),
            "tp_today":        int(pred_today["tp"]    or 0),
            "fp_today":        int(pred_today["fp"]    or 0),

            # --- processing time (segons per alerta, des de l'inici a auto_splunk
            #     fins a la classificació; exclou el temps de cua) ---
            "proc_count":       int(proc["cnt"]   or 0),
            "proc_avg":         r2(proc["avg_s"]),
            "proc_p50":         r2(proc["p50"]),
            "proc_p95":         r2(proc["p95"]),
            "proc_min":         r2(proc["min_s"]),
            "proc_max":         r2(proc["max_s"]),
            "proc_count_today": int(proc_today["cnt"] or 0),
            "proc_avg_today":   r2(proc_today["avg_s"]),
        })

    except Exception as e:
        return jsonify({"error": str(e)}), 500


