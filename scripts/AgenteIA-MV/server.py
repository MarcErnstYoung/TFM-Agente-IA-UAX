@app.route("/api/charts/score_distribution", methods=["GET"])
def api_charts_score_distribution():
    """
    Returns score distribution in buckets of 0.05 width.
    Useful to render a histogram of model confidence.

    Response shape:
    {
        "rows": [
            { "bucket": "0.00-0.05", "count": 34, "tp": 0,  "fp": 34 },
            { "bucket": "0.25-0.30", "count": 12, "tp": 8,  "fp": 4  },
            { "bucket": "0.30-0.35", "count": 45, "tp": 45, "fp": 0  },
            ...
        ],
        "threshold": 0.29352656
    }
    """
    try:
        rows = query(
            get_conn,
            """
            SELECT
                FLOOR(score / 0.05) * 0.05            AS bucket_start,
                COUNT(*)                               AS count,
                SUM(CASE WHEN prediction = true  THEN 1 ELSE 0 END) AS tp,
                SUM(CASE WHEN prediction = false THEN 1 ELSE 0 END) AS fp
            FROM predicted.eventos
            WHERE score IS NOT NULL
            GROUP BY bucket_start
            ORDER BY bucket_start ASC
            """
        )

        # Format bucket label as "0.25-0.30"
        formatted = []
        for r in rows:
            start = float(r["bucket_start"])
            end   = round(start + 0.05, 2)
            formatted.append({
                "bucket": f"{start:.2f}-{end:.2f}",
                "count":  int(r["count"]),
                "tp":     int(r["tp"]),
                "fp":     int(r["fp"]),
            })

        return jsonify({"rows": formatted, "threshold": 0.29352656})

    except Exception as e:
        return jsonify({"error": str(e)}), 500


